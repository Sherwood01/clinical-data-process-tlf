    def generate_program(
        self,
        table_id: str,
        table_name: str,
        analysis_set: str,
        group_by: str,
        dataset: str,
        macro_calls: list[dict] = None,
        population_filter: str = "1=1"
    ) -> str:
        """
        Generate a complete SAS TLF program matching reference format.

        Args:
            table_id: Table identifier (e.g., "14.1.1.1")
            table_name: Table name
            analysis_set: Analysis set name
            group_by: Grouping variable (usually treatment)
            dataset: ADaM dataset name
            macro_calls: List of macro call specifications (optional)
            population_filter: Population filter condition

        Returns:
            Generated SAS program code
        """
        from datetime import datetime

        dataset_upper = dataset.upper()

        # Build header
        header = self.HEADER_TEMPLATE.format(
            table_id=table_id,
            table_name=table_name,
            analysis_set=analysis_set,
            group_by=group_by,
            dataset_upper=dataset_upper,
            timestamp=datetime.now().strftime("%Y-%m-%d"),
            data_prep=self.DATA_PREP_TEMPLATE.strip()
        )

        # Generate analysis units based on table_id
        if table_id == "14.1.1.1":
            analysis_units = self._generate_14111_units()
        else:
            # Default units for other tables
            analysis_units = self._generate_default_units(dataset_upper, group_by)

        # Build footer
        footer = self.FOOTER_TEMPLATE.format(unit_prefix="unit")

        # Combine
        full_code = header + "\n".join(analysis_units) + footer
        return full_code

    def _generate_14111_units(self) -> list[str]:
        """Generate analysis units for Table 14.1.1.1 - matches reference exactly."""
        units = []
        for unit_def in self.TABLE_14111_UNITS:
            if unit_def["type"] == "Discrete_simple":
                unit_code = self.DISCRETE_SIMPLE_UNIT.format(**unit_def)
