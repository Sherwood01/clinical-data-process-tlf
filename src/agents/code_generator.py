    def validate_syntax(self, code: str) -> dict:
        """Validate SAS code syntax."""
        errors = []
        warnings = []

        # Check balanced quotes
        single_quotes = code.count("'")
        if single_quotes % 2 != 0:
            errors.append("Unbalanced single quotes")

        # Check balanced parentheses
        paren_count = 0
        for char in code:
            if char == '(':
                paren_count += 1
            elif char == ')':
                paren_count -= 1
            if paren_count < 0:
                errors.append("Unexpected closing parenthesis")
                break

        if paren_count != 0:
            errors.append("Unbalanced parentheses")

        # Check for required elements
        if "%include" not in code:
            warnings.append("Missing %include statement")

        if "%import" not in code:
            warnings.append("Missing %import statement")

        if "%output" not in code:
            warnings.append("Missing %output statement")

        if "%Discrete_simple" not in code:
            warnings.append("Missing %Discrete_simple macro call")

        return {
            "valid": len(errors) == 0,
            "errors": errors,
            "warnings": warnings
        }
